export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) {
    return res.status(500).json({ error: 'שגיאת הגדרה. פני אליי בוואטסאפ.' });
  }

  try {
    const { history } = req.body || {};
    if (!Array.isArray(history) || history.length === 0) {
      return res.status(400).json({ error: 'לא התקבלה הודעה' });
    }

    // Gemini uses "user" and "model" roles
    let contents = history.map(m => ({
      role: m.role === 'assistant' ? 'model' : 'user',
      parts: [{ text: m.content }]
    }));

    // Gemini requires conversation to start with a 'user' message.
    // Strip the client-side opening message.
    while (contents.length > 0 && contents[0].role !== 'user') {
      contents.shift();
    }

    if (contents.length === 0) {
      return res.status(400).json({ error: 'לא התקבלה הודעה' });
    }

    const geminiUrl = `https://generativelanguage.googleapis.com/v1beta/models/gemini-flash-latest:generateContent?key=${apiKey}`;

    const geminiRes = await fetch(geminiUrl, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        systemInstruction: {
          parts: [{ text: SYSTEM_PROMPT }]
        },
        contents,
        generationConfig: {
          temperature: 0.6,
          topP: 0.9,
          maxOutputTokens: 1024,
          thinkingConfig: { thinkingBudget: 0 }
        }
      })
    });

    if (!geminiRes.ok) {
      const errText = await geminiRes.text();
      console.error('Gemini error status:', geminiRes.status);
      console.error('Gemini error body:', errText);

      if (geminiRes.status === 429) {
        return res.status(200).json({
          reply: 'השירות עמוס כרגע. נסי שוב בעוד רגע, או פני אליי ישירות בוואטסאפ: [לחצי כאן](https://wa.me/972526917789)'
        });
      }
      const shortErr = errText.substring(0, 200);
      return res.status(500).json({ error: `שגיאה (${geminiRes.status}): ${shortErr}` });
    }

    const data = await geminiRes.json();
    const reply = data?.candidates?.[0]?.content?.parts?.[0]?.text;

    if (!reply) {
      console.error('Empty reply. Full response:', JSON.stringify(data).substring(0, 500));
      return res.status(500).json({ error: 'לא התקבלה תשובה. נסי שוב.' });
    }

    return res.status(200).json({ reply: reply.trim() });
  } catch (err) {
    console.error('Chat handler error:', err);
    return res.status(500).json({ error: 'משהו השתבש. נסי שוב או פני אליי בוואטסאפ.' });
  }
}
